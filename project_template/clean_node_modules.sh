PATH_TO_SCRIPT=$(realpath "$0")
PATH_TO_SCRIPT_DIRECTORY=$(dirname "$PATH_TO_SCRIPT")
PATH_TO_PACKAGES_DIR=${PATH_TO_SCRIPT_DIRECTORY}/packages
PATH_TO_TOP_LEVEL_NODE_MODULES=${PATH_TO_SCRIPT_DIRECTORY}/node_modules

echo "Removing ${PATH_TO_TOP_LEVEL_NODE_MODULES}"
rm -rf "${PATH_TO_TOP_LEVEL_NODE_MODULES}"

for sub_directory in `find ${PATH_TO_PACKAGES_DIR} -type d -maxdepth 1 -mindepth 1`
do
  NODE_MODULES_DIR=${sub_directory}/node_modules
  echo "Removing ${NODE_MODULES_DIR}"
  rm -rf ${NODE_MODULES_DIR}
done

